#!/bin/sh
if [[ $1 = "nobuild" ]]; then
    echo Skipping Build
else
    make airbloc &> ./build/airbloc.log
    if [[ $? -eq 0 ]]; then
        rm ./build/airbloc.log
        echo Build successful.
        sleep 1
    else
        cat ./build/airbloc.log
        echo Build failed. See log for details.
        exit 1
    fi
fi

# Launch Airbloc tmux
tmux new -d -s airbloc
tmux rename-window 'Airbloc'
tmux send-keys "./build/bin/airbloc server -c local/provider.yml --verbose" "C-m"
tmux split-window -v
tmux send-keys "./build/bin/airbloc controller -c local/controller.yml --verbose" "C-m"
tmux -2 attach-session -t airbloc

if [[ $1 = "test" ]]; then
    echo Removing airbloc session.
    tmux kill-session -t airbloc
fi
